def rgb_color_gen():
    rgb_color = []

    for i in range(3):
        rgb_color.append(randint(0, 255))

    rgb_color = tuple(rgb_color)

    return rgb_color


print(rgb_color_gen())


print("\nGenerated list of hexadecimal colors ")
def list_of_hexa_colors():
    characters = string.ascii_letters[:6] + string.digits

    hexa_colors = "#"

    for _ in range(6):
        hexa_colors += choice(characters)

    return hexa_colors


print(list_of_hexa_colors())


print("\nGenerated list of hexadecimal colors ")
def list_of_rgb_colors(nb):
    list_rgb_colors = []

    for _ in range(nb):
        rgb_colors = []

        for _ in range(3):
            rgb_colors.append(randint(0, 255))

        list_rgb_colors.append(f"rgb{tuple(rgb_colors)}")

    return list_rgb_colors


print(list_of_rgb_colors(5))


print("\nGenerated list of hex and rgb colors")
def generate_colors(color_type, color_number):
    if color_type == 'hexa':
        list_hexa_colors = []
        for _ in range(color_number):
            list_hexa_colors.append(list_of_hexa_colors())

        return list_hexa_colors

    elif color_type == 'rgb':
        return list_of_rgb_colors(color_number)

    return None


print(generate_colors("rgb", 4))



print("\nShuffle list")
def shuffle_list(list_n):
    list_shuffle = []
    for l in range(len(list_n)):
        list_shuffle.append(choice(list_n))

    return list_shuffle


print(shuffle_list([51, 62, 28, 14, 32]))